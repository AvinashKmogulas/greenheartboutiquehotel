    document.querySelectorAll('.booking-offer-link').forEach(link => {
    link.addEventListener('click', function(event) {
        event.preventDefault(); // Prevent the default link click behavior

        const baseUrl = 'https://hotels.cloudbeds.com/en/reservation/NC3rR2/?currency=usd';
        const today = new Date();
        const tomorrow = new Date(today);
        tomorrow.setDate(today.getDate() + 1);

        // Format the dates in YYYY-MM-DD format
        const formattedToday = today.toISOString().split('T')[0];
        const formattedTomorrow = tomorrow.toISOString().split('T')[0];

        // Construct the final URL with date parameters
        const finalUrl = `${baseUrl}&checkin=${formattedToday}&checkout=${formattedTomorrow}`;

        // Open the URL in a new window/tab
        window.open(finalUrl, '_blank');
    });
});
</script>